def H(text, level=1):
    hd = doc.add_heading(text, level=level)
    for run in hd.runs:
        run.font.name = 'Microsoft YaHei'
        run._element.rPr.rFonts.set(qn('w:eastAsia'), 'Microsoft YaHei')
    return hd

def shade(cell, color):
    s = OxmlElement('w:shd'); s.set(qn('w:fill'), color); s.set(qn('w:val'), 'clear')
    cell._tc.get_or_add_tcPr().append(s)

def T(headers, rows):
    t = doc.add_table(rows=1+len(rows), cols=len(headers))
    t.style = 'Table Grid'; t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, hdr in enumerate(headers):
        c = t.rows[0].cells[i]; c.text = hdr
        for p in c.paragraphs:
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            for r in p.runs: r.bold = True; r.font.size = Pt(10); r.font.color.rgb = RGBColor(255,255,255)
        shade(c, '2563EB')
    for ri, row in enumerate(rows):
        for ci, val in enumerate(row):
            c = t.rows[ri+1].cells[ci]; c.text = str(val)
            for p in c.paragraphs:
                for r in p.runs: r.font.size = Pt(10)
            if ri % 2 == 1: shade(c, 'F1F5F9')
    doc.add_paragraph()
    return t

def B(text): doc.add_paragraph(text, style='List Bullet')
def N(text): doc.add_paragraph(text, style='List Number')
def P(text): doc.add_paragraph(text)

def COPY(title, body, note=None):
    """统一格式的一段文案"""
    p = doc.add_paragraph()
    r = p.add_run(f'【{title}】')
    r.bold = True; r.font.size = Pt(11); r.font.color.rgb = RGBColor(37,99,235)
